namespace Heroes.Element.Types;

/// <summary>
/// Specifies the armor set.
/// </summary>
public enum ArmorSet
{
    /// <summary>
    /// Indicates an unknown armor set.
    /// </summary>
    Unknown,

    /// <summary>
    /// Indicates a hero armor set.
    /// </summary>
    Hero,

    /// <summary>
    /// Indicates a mercenary armor set.
    /// </summary>
    Merc,

    /// <summary>
    /// Indicates a monster armor set.
    /// </summary>
    Monster,

    /// <summary>
    /// Indicates a summon armor set.
    /// </summary>
    Summon,

    /// <summary>
    /// Indicates a structure armor set.
    /// </summary>
    Structure,

    /// <summary>
    /// Indicates a minion armor set.
    /// </summary>
    Minion,
}
