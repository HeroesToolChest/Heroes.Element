namespace Heroes.Element.Models.Meta;

/// <summary>
/// Specifies the type of the items in a json file.
/// </summary>
public enum ItemsType
{
    /// <summary>
    /// Indicates the items are of an <c>other</c> type.
    /// </summary>
    Other,

    /// <summary>
    /// Indicates the items are of <c>data</c> type.
    /// </summary>
    Data,

    /// <summary>
    /// Indicates the items are of <c>gamestrings</c> type.
    /// </summary>
    GameStrings,
}
